      <template v-if="device!=='mobile'">
        <search id="header-search" class="right-menu-item" />

        <el-tooltip content="消息提醒" effect="dark" placement="bottom">
          <message id="message" class="right-menu-item hover-effect" />
        </el-tooltip>

